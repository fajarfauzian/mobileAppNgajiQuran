class Lesson {
  final int id;
  final int materiId;
  final String title;
  final String description;
  final bool isCompleted;
  final int score;
  final String content;

  Lesson({
    required this.id,
    required this.materiId,
    required this.title,
    required this.description,
    required this.isCompleted,
    required this.score,
    required this.content,
  });
}